export function prepareArabicForSpeech(text: string, language: "ar" | "en", dialect: "egyptian" | "gulf" | "levantine" | "fusha" | "english" | "gaming" = "fusha") {
  if (language !== "ar" || !arabicTextPattern.test(text)) {
    return text;
  }

  let speechText = text;

  if (dialect === "egyptian") {
    speechText = speechText
      .replace(/أنت/g, "إنت")
      .replace(/أَنتَ/g, "إنت")
      .replace(/أنا معك/g, "أنا معاك")
      .replace(/خطوة واحدة الآن/g, "خطوة واحدة دلوقتي");
  } else if (dialect === "gulf" || dialect === "gaming") {
    speechText = speechText
      .replace(/أنت/g, "إنت")
      .replace(/أنا معك/g, "أنا وياك")
      .replace(/خطوة واحدة الآن/g, "خطوة وحدة الحين")
      .replace(/محتاج/g, "تحتاج");
  } else if (dialect === "levantine") {
    speechText = speechText
      .replace(/أنت/g, "إنت")
      .replace(/أنا معك/g, "أنا معك")
      .replace(/خطوة واحدة الآن/g, "خطوة وحدة هلا");
  }

  const phraseAdjusted = phraseTashkeel.reduce((currentText, [plain, diacritized]) => {
    return currentText.replace(new RegExp(escapeRegExp(plain), "g"), diacritized);
  }, speechText);

  return phraseAdjusted.replace(/[\u0621-\u064A]+/g, (word) => wordTashkeel[word] ?? word);
}

function escapeRegExp(value: string) {
  return value.replace(/[.*+?^${}()|[\]\\]/g, "\\$&");
}